# This code is a simple band name generator. It prompts the user to enter the city they grew up in and
# the name of a pet. It then concatenates the city and pet names together to create a band name and
# prints it out.
print("Welcome to the band name generator.")
city = input("Which city did you grow up in?\n")
pet = input("What is the name of a pet?\n")
print("Your band name could be " + city + " " + pet)
